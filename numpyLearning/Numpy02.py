""" Some Mathematical functions used in 'numpy' """

import numpy as np

arr = np.array([0, 1, 2, 3, 4, 5])

'''sum() sum function returns sum of all elements of numpy array'''
print(arr.sum())

'''std() function returns the standard deviation of elements of given array'''
print(arr.std())

'''mean() function returns the mean of all elements of array'''
print(arr.mean())

'''max() function returns the maximum element of an array'''
print(arr.max())

'''min() function returns the minimum element of an array'''
print(arr.min())
